// Get sign of 12.34.
console.log(Math.sign(12.34)); // 1
// Get sign of -12.34.
console.log(Math.sign(-12.34)); // -1

// Get sign of 0.987.
console.log(Math.sign(0.987)); // 1
// Get sign of -0.987.
console.log(Math.sign(-0.987)); // -1

// Get sign of 0.
console.log(Math.sign(0)); // 0
// Get sign of -0.
console.log(Math.sign(-0)); // -0

// Get sign of NaN.
console.log(Math.sign(NaN)); // NaN
// Get sign of null.
console.log(Math.sign(null)); // 0
// Get sign of nothing.
console.log(Math.sign()); // NaN